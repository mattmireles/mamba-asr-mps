# MARK: - Statistical Analysis Functions

def percentile(data: List[float], p: float) -> float:
    """
    Compute percentile using linear interpolation for accurate latency analysis.
    
    This function implements the linear interpolation method for percentile calculation,
    providing more accurate estimates than nearest-neighbor approaches. It's specifically
    optimized for latency analysis where precise percentile estimation is critical for
    SLA validation and deployment readiness assessment.
    
    Mathematical Approach:
    - Linear interpolation: Provides smooth percentile estimates between data points
    - Index calculation: Uses fractional indexing for precise percentile positioning
    - Boundary handling: Proper edge case management for small datasets
    - Numerical stability: Avoids floating-point precision issues in index calculation
    
    Algorithm Details:
    1. Fractional index: k = (n-1) * (p/100) for percentile position
    2. Floor/ceiling: Split fractional index into integer bounds
    3. Interpolation: Weighted average between adjacent data points
    4. Edge cases: Direct return for exact index matches (no interpolation needed)
    
    Called By:
    - main() function for computing p50, p90, p99 latency percentiles
    - Statistical analysis workflows requiring accurate percentile estimation
    - Performance validation scripts for SLA compliance checking
    
    Performance Characteristics:
    - Time complexity: O(1) after data sorting (caller responsibility)
    - Space complexity: O(1) constant memory usage
    - Numerical precision: Maintains float64 precision throughout calculation
    - Edge case handling: Returns 0.0 for empty datasets (graceful degradation)
    
    Args:
        data: Sorted list of latency measurements in milliseconds
              - Must be pre-sorted in ascending order for correct results
              - Typically contains streaming inference timing measurements
              - Empty list handled gracefully with 0.0 return value
        p: Percentile value between 0 and 100 (inclusive)
           - Common values: 50 (median), 90, 95, 99 for SLA analysis
           - Supports fractional percentiles for precise analysis
           
    Returns:
        Interpolated percentile value in same units as input data
        - Returns 0.0 for empty datasets (error handling)
        - Maintains input precision for downstream analysis
        
    Example Usage:
        latencies = [1.2, 2.1, 3.5, 4.8, 12.3]  # Pre-sorted
        p50 = percentile(latencies, 50.0)  # Median latency
        p90 = percentile(latencies, 90.0)  # 90th percentile SLA
        p99 = percentile(latencies, 99.0)  # 99th percentile outlier
        
    Mathematical Foundation:
        For percentile p in dataset of size n:
        - Fractional index: k = (n-1) * p/100
        - Floor index: f = floor(k)
        - Ceiling index: c = min(f+1, n-1)
        - Interpolated value: data[f] * (c-k) + data[c] * (k-f)
    """
    if not data:
        return 0.0
    
    # Calculate fractional index for percentile position
    # Using (n-1) for proper indexing into 0-based array
    k = (len(data) - 1) * (p / 100.0)
    
    # Split fractional index into integer bounds
    f = int(k)  # Floor index
    c = min(f + 1, len(data) - 1)  # Ceiling index with bounds check
    
    # Handle exact index match (no interpolation needed)
    if f == c:
        return data[f]
    
    # Linear interpolation between adjacent data points
    # Weighted average based on fractional position
    d0 = data[f] * (c - k)  # Weight toward lower bound
    d1 = data[c] * (k - f)  # Weight toward upper bound
    return d0 + d1

# MARK: - Configuration Constants

class LatencyAnalysisConstants:
    """
    Named constants for latency analysis configuration and reporting standards.
    
    These constants define the operational parameters for latency measurement
    analysis, ensuring consistent behavior across different CSV input formats
    and maintaining standardized output formatting for deployment validation.
    """
    
    # MARK: - CSV Format Configuration
    
    # Standard column name for latency measurements in milliseconds.
    # Used by MambaASRRunner when generating structured CSV output
    # with named columns for automated processing workflows.
    LATENCY_COLUMN_NAME = "latency_ms"
    
    # Minimum required columns for unnamed CSV format parsing.
    # Fallback format: column 0 = chunk_id, column 1 = latency_ms
    # Ensures compatibility with simplified CSV output formats.
    MIN_UNNAMED_COLUMNS = 2
    
    # Index of latency column in unnamed CSV format.
    # Second column (index 1) contains latency measurements
    # when CSV lacks header row or named columns.
    LATENCY_COLUMN_INDEX = 1
    
    # MARK: - Statistical Analysis Configuration
    
    # Percentile values for deployment SLA analysis.
    # These percentiles provide comprehensive latency characterization
    # for real-time speech recognition deployment validation.
    MEDIAN_PERCENTILE = 50.0      # Typical user experience latency
    P90_PERCENTILE = 90.0         # Standard SLA threshold for real-time systems  
    P99_PERCENTILE = 99.0         # Outlier analysis for worst-case scenarios
    
    # Decimal precision for latency measurements in report output.
    # 3 decimal places provides millisecond-level precision suitable
    # for real-time inference latency analysis and SLA validation.
    PRECISION_DECIMAL_PLACES = 3
    
    # MARK: - Report Generation Configuration
    
    # Markdown report title for latency analysis documentation.
    # Standardized heading for automated report aggregation
    # and documentation pipeline integration.
    REPORT_TITLE = "## Streaming latency summary"
    
    # Markdown table header for structured latency metric display.
    # Provides consistent formatting across different analysis runs
    # and enables automated report parsing workflows.
    TABLE_HEADER = "| metric | ms |\n|---|---:|"


# MARK: - Main Analysis and Reporting Function

def main() -> None:
    """
    Execute comprehensive latency analysis and generate standardized performance report.
    
    This function orchestrates the complete latency analysis workflow by parsing
    CSV input files, computing statistical metrics, and generating markdown reports
    for deployment validation and performance documentation workflows.
    
    Analysis Workflow:
    1. CSV parsing: Flexible format detection and latency data extraction
    2. Data validation: Error handling for malformed entries and missing values
    3. Statistical computation: Percentile analysis for SLA validation metrics
    4. Report generation: Markdown table output for documentation integration
    5. Directory management: Output path creation for automated workflow integration
    
    Called By:
    - Command line execution: Direct script invocation for manual analysis
    - CI/CD pipelines: Automated performance validation after model validation
    - MambaASRRunner: Integration via --latency-csv flag for structured reporting
    - Performance analysis workflows: Batch processing of multiple validation runs
    
    Calls:
    - argparse.ArgumentParser for command-line interface and automation integration
    - csv.DictReader/csv.reader for flexible CSV format parsing and data extraction
    - percentile() for accurate statistical analysis of latency measurements
    - os.makedirs() for output directory structure creation and file management
    
    CSV Format Support:
    1. Named columns: Preferred format with "chunk,latency_ms" header row
    2. Unnamed columns: Fallback format with implicit column ordering
    3. Error recovery: Graceful handling of malformed entries and parsing errors
    4. Flexible parsing: Automatic format detection for workflow integration
    
    Statistical Analysis:
    - Count: Total number of valid latency measurements for sample size validation
    - Average: Mean latency for typical performance characterization
    - p50 (median): Central tendency for typical user experience assessment
    - p90: Standard SLA threshold for real-time system performance validation
    - p99: Outlier analysis for worst-case latency scenario planning
    
    Output Format:
    - Markdown table: Human-readable format for documentation integration
    - Standardized metrics: Consistent reporting across different model variants
    - Precision formatting: 3 decimal places for millisecond-level accuracy
    - Console summary: Quick validation results for manual execution feedback
    
    Error Handling:
    - Invalid CSV entries: Skipped with graceful error recovery
    - Missing files: Clear error messages for debugging workflow issues
    - Malformed data: Robust parsing that continues despite individual entry errors
    - Directory creation: Automatic output path management for reliability
    
    Performance Validation Context:
    - Real-time targets: <10ms inference latency for speech recognition deployment
    - SLA compliance: p90 and p99 metrics for production system validation
    - Apple Silicon: Unified memory architecture performance characterization
    - Deployment readiness: Statistical confirmation of acceptable latency distribution
    """
    # Configure command-line interface for automation integration
    ap = argparse.ArgumentParser(description="Summarize latency CSV (chunk,latency_ms)")
    ap.add_argument("--csv", required=True, help="Path to latency CSV")
    ap.add_argument("--out", required=True, help="Output markdown path")
    args = ap.parse_args()

    # Parse latency measurements with flexible CSV format support
    lat: List[float] = []
    with open(args.csv, newline="") as f:
        r = csv.DictReader(f)
        
        # Attempt named column parsing (preferred format)
        if r.fieldnames and LatencyAnalysisConstants.LATENCY_COLUMN_NAME in r.fieldnames:
            for row in r:
                try:
                    lat.append(float(row[LatencyAnalysisConstants.LATENCY_COLUMN_NAME]))
                except (ValueError, TypeError, KeyError):
                    # Skip malformed entries with graceful error recovery
                    pass
        else:
            # Fallback to unnamed column parsing for compatibility
            f.seek(0)
            r2 = csv.reader(f)
            next(r2, None)  # Skip potential header row
            for row in r2:
                if len(row) >= LatencyAnalysisConstants.MIN_UNNAMED_COLUMNS:
                    try:
                        lat.append(float(row[LatencyAnalysisConstants.LATENCY_COLUMN_INDEX]))
                    except (ValueError, TypeError, IndexError):
                        # Skip malformed entries with graceful error recovery
                        pass

    # Prepare data for statistical analysis
    lat.sort()  # Required for accurate percentile computation
    count = len(lat)
    
    # Compute comprehensive statistical metrics for deployment validation
    avg = statistics.mean(lat) if lat else 0.0
    p50 = percentile(lat, LatencyAnalysisConstants.MEDIAN_PERCENTILE)
    p90 = percentile(lat, LatencyAnalysisConstants.P90_PERCENTILE)
    p99 = percentile(lat, LatencyAnalysisConstants.P99_PERCENTILE)

    # Ensure output directory exists for reliable file generation
    _out_dir = os.path.dirname(args.out)
    if _out_dir:
        os.makedirs(_out_dir, exist_ok=True)
    
    # Generate standardized markdown report
    with open(args.out, "w") as f:
        f.write(f"{LatencyAnalysisConstants.REPORT_TITLE}\n\n")
        f.write(f"{LatencyAnalysisConstants.TABLE_HEADER}\n")
        f.write(f"| count | {count} |\n")
        f.write(f"| avg | {avg:.{LatencyAnalysisConstants.PRECISION_DECIMAL_PLACES}f} |\n")
        f.write(f"| p50 | {p50:.{LatencyAnalysisConstants.PRECISION_DECIMAL_PLACES}f} |\n")
        f.write(f"| p90 | {p90:.{LatencyAnalysisConstants.PRECISION_DECIMAL_PLACES}f} |\n")
        f.write(f"| p99 | {p99:.{LatencyAnalysisConstants.PRECISION_DECIMAL_PLACES}f} |\n")
    
    # Provide console feedback for manual execution and debugging
    print(f"Wrote {args.out} with {count} samples: "
          f"avg={avg:.{LatencyAnalysisConstants.PRECISION_DECIMAL_PLACES}f} "
          f"p50={p50:.{LatencyAnalysisConstants.PRECISION_DECIMAL_PLACES}f} "
          f"p90={p90:.{LatencyAnalysisConstants.PRECISION_DECIMAL_PLACES}f} "
          f"p99={p99:.{LatencyAnalysisConstants.PRECISION_DECIMAL_PLACES}f}")


if __name__ == "__main__":
    main()
